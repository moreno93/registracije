public class Cars {
    private String reg;
    private String tip;
    private String sasija;
    private String kuca;
    private String polica;

    public String getReg() {
        return reg;
    }

    public void setReg(String reg) {
        this.reg = reg;
    }

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }

    public String getSasija() {
        return sasija;
    }

    public void setSasija(String sasija) {
        this.sasija = sasija;
    }

    public String getKuca() {
        return kuca;
    }

    public void setKuca(String kuca) {
        this.kuca = kuca;
    }

    public String getPolica() {
        return polica;
    }

    public void setPolica(String polica) {
        this.polica = polica;
    }


}
